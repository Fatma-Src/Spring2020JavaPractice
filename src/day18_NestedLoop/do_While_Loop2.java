package day18_NestedLoop;

public class do_While_Loop2 {
    public static void main(String[] args) {

        int number = 103;

        while(number>=100){
            System.out.println(number);
            number--;
        }

        System.out.println("=============================================");

        int number2=103;
        do{System.out.println(number);
            number2--;

        }while(number2>=100);

        //the difference betwwe these , if condition true they give same result but false do  while




    }
}
